#Break, Continue



#Break
#کلا ميپره بيرون
for char in "jadi is teacher":
    if (char==' '):
        break
    print(char)
    
print("end of loop")




#Continue
#يعني همين يبار رو بيخيال شو
for char in "jadi is teacher":
    if (char==' '):
        continue
    print(char)
    
print("end of loop")
